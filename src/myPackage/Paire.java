package myPackage;

public class Paire <T> {
	 private T premie;
	 private T deuxieme;
     public Paire()
			     {
			    	premie=null;
			    	deuxieme=null;
			     }
     public Paire (T premie, T deuxieme)
			     {
			    	 this.premie=premie;
			    	 this.deuxieme=deuxieme;
			     }
 
     
}
